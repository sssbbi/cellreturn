<script type="text/javascript">
	function talentBg() {
		$('.cellreturner-talent-list').on('mouseenter',function(){
			var str = '../assets/images/sub/cellreturner_talent_bg'+($(this).index()+1)+'.jpg';

			$('.cellreturner-talent').css({'background-image':'url('+str+')'});
			$('.cellreturner-talent-list').removeClass('on');
			$(this).addClass('on');
		});
	}
	$(document).ready(function(){
		if ( $(window).width() > 768 ) {
			talentBg();
		}
	});
	$(window).resize(function(){
		if ( $(window).width() > 768 ) {
			talentBg();
		}
	});
</script>
