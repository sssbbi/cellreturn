<script type="text/javascript">
	var oriImg = '../assets/images/about/map_marker.png',
			onImg = '../assets/images/about/map_marker_on.png';

	$('.global-map-wrap .marker-bx > li').mouseenter(function(){
		$(this).addClass('active');
		$(this).find('img').attr('src',onImg);
	});
	$('.global-map-wrap .marker-bx > li').mouseleave(function(){
		$(this).removeClass('active');
		$(this).find('img').attr('src',oriImg);
	});

	$(document).on('click', '.global-map-wrap .marker-bx > li', function(){
		if ( $(this).find('.pop-info').css('display') == 'none' ) {
			$('.pop-info').hide();
			$(this).find('.pop-info').show();
			$('.global-map-wrap .marker-bx > li').removeClass('on');
			$(this).addClass('on');
		} else {
			$(this).find('.pop-info').hide();
			$(this).removeClass('on');
		}
	});
</script>

